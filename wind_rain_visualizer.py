import os, gc, shutil, psutil
import xarray as xr
import numpy as np
import matplotlib.pyplot as plt
import cartopy.crs as ccrs
import cartopy.feature as cfeature
import imageio.v2 as imageio
from datetime import datetime, timedelta
from herbie import Herbie

# --- SETTINGS ---
os.environ["ECCODES_DEFINITION_PATH"] = "/usr/local/share/eccodes/definitions"
os.environ["ECCODES_SAMPLES_PATH"] = "/usr/local/share/eccodes/samples"

base_dir = "/content/drive/MyDrive/OPIM5516/M_T/M_T_Wind_Dorian_Test"
img_dir = os.path.join(base_dir, "img_data")
grib_dir = os.path.join(base_dir, "grib_data")
video_dir = os.path.join(base_dir, "monthly_gifs")

os.makedirs(img_dir, exist_ok=True)
os.makedirs(grib_dir, exist_ok=True)
os.makedirs(video_dir, exist_ok=True)

# CHANGE THE DISPLAY DATE RANGE HERE
start_date = datetime(2019, 9, 1, 0)
end_date = datetime(2019, 9, 7, 23)

# Southeast / Florida region
lon_min, lon_max = -90, -70
lat_min, lat_max = 24, 38

# Downsample factor
# Larger number = faster but blurrier
skip = 4

img_files = []
t_step = start_date

while t_step <= end_date:
    timestamp_str = f"{t_step:%Y%m%d_%H}"
    png_path = os.path.join(img_dir, f"wind_{timestamp_str}.png")
    grib_path = os.path.join(grib_dir, f"wind_{timestamp_str}.grib2")

    print(f"Processing {timestamp_str}...")

    try:
        # HRRR surface data for this hour
        H = Herbie(
            t_step,
            model="hrrr",
            product="sfc",
            fxx=0,
            priority="aws",
            verbose=False
        )

        # Download 10-meter U and V wind components
        wind_file = H.download(":(?:U|V)GRD:10 m above ground")

        if not os.path.exists(grib_path):
            shutil.copy(wind_file, grib_path)

        # Open the wind data
        with xr.open_dataset(
            wind_file,
            engine="cfgrib",
            backend_kwargs={
                "filter_by_keys": {
                    "typeOfLevel": "heightAboveGround",
                    "level": 10
                }
            }
        ) as ds:

            u10 = ds["u10"]
            v10 = ds["v10"]

            # Calculate total wind speed
            wind_speed_ms = np.sqrt(u10**2 + v10**2)
            wind_speed_mph = wind_speed_ms * 2.23694

            # Convert longitude from 0–360 to -180–180
            lon = (((ds.longitude + 180) % 360) - 180).compute()
            lat = ds.latitude.compute()

            # Find grid cells inside our region
            region_mask = (
                (lon >= lon_min) & (lon <= lon_max) &
                (lat >= lat_min) & (lat <= lat_max)
            )

            # Convert the mask into row/column positions
            yy, xx = np.where(region_mask.values)

            if len(yy) == 0 or len(xx) == 0:
                raise ValueError("No HRRR grid points found inside the selected map region.")

            # Get the rectangular box around the region
            y0, y1 = yy.min(), yy.max() + 1
            x0, x1 = xx.min(), xx.max() + 1

            # Crop and downsample the wind + coordinates
            wind_region = wind_speed_mph.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).compute()

            lon_region = lon.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).values

            lat_region = lat.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).values

            wind_values = wind_region.values

        # --- MAKE MAP ---
        fig = plt.figure(figsize=(9, 7))
        ax = plt.axes(projection=ccrs.PlateCarree())

        ax.set_extent([lon_min, lon_max, lat_min, lat_max], crs=ccrs.PlateCarree())

        ax.add_feature(cfeature.STATES.with_scale("50m"), linewidth=0.5)
        ax.add_feature(cfeature.COASTLINE.with_scale("50m"))
        ax.add_feature(cfeature.BORDERS.with_scale("50m"))

        mesh = ax.pcolormesh(
            lon_region,
            lat_region,
            wind_values,
            transform=ccrs.PlateCarree(),
            cmap="viridis",
            vmin=0,
            vmax=80,
            shading="auto"
        )

        cbar = plt.colorbar(mesh, ax=ax, shrink=0.75)
        cbar.set_label("10m Wind Speed (mph)")

        plt.title(f"HRRR 10m Wind Speed | {t_step:%Y-%m-%d %H}Z")

        plt.savefig(png_path, bbox_inches="tight", dpi=120)
        plt.close("all")

        img_files.append(png_path)
        print(f"✅ Saved image: {png_path}")

        del wind_speed_ms, wind_speed_mph, wind_region, wind_values, lon_region, lat_region
        gc.collect()

    except Exception as e:
        print(f"⚠️ Failed {timestamp_str}: {e}")
        plt.close("all")

    t_step += timedelta(hours=1)

# --- BUILD TEST GIF ---
if img_files:
    gif_path = os.path.join(video_dir, "wind_animation_dorian_test.gif")
    print("Creating test GIF...")

    with imageio.get_writer(gif_path, mode="I", duration=300, loop=0) as writer:
        for file in sorted(img_files):
            writer.append_data(imageio.imread(file))

    print(f"✅ Saved GIF: {gif_path}")
else:
    print("No images were created, so no GIF was made.")

print("Done.")
print(f"Current Memory: {psutil.virtual_memory().used / 1e9:.2f} GB")

