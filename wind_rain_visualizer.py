while t_step <= end_date:
    timestamp_str = f"{t_step:%Y%m%d_%H}"
    png_path = os.path.join(img_dir, f"rain_{timestamp_str}.png")
    grib_path = os.path.join(grib_dir, f"rain_{timestamp_str}.grib2")

    print(f"Processing rain for {timestamp_str}...")

    try:
        # For hourly rainfall ending at t_step, use the previous model run with fxx=1
        run_time = t_step - timedelta(hours=1)

        H = Herbie(
            run_time,
            model="hrrr",
            product="sfc",
            fxx=1,
            priority="aws",
            verbose=False
        )

        # Download hourly accumulated precipitation
        rain_file = H.download("APCP:surface")

        if not os.path.exists(grib_path):
            shutil.copy(rain_file, grib_path)

        with xr.open_dataset(
            rain_file,
            engine="cfgrib",
            backend_kwargs={
                "filter_by_keys": {
                    "typeOfLevel": "surface"
                }
            }
        ) as ds:

            # Usually the precipitation variable is named "tp"
            # but this makes it robust in case cfgrib names it differently.
            possible_names = ["tp", "apcp", "unknown"]

            rain_var_name = None
            for name in possible_names:
                if name in ds.data_vars:
                    rain_var_name = name
                    break

            if rain_var_name is None:
                rain_var_name = list(ds.data_vars)[0]

            rain_mm = ds[rain_var_name]

            # HRRR precipitation is usually in kg/m^2, which equals mm of water.
            # Convert mm to inches for easier interpretation.
            rain_inches = rain_mm / 25.4

            # Convert longitude from 0–360 to -180–180
            lon = (((ds.longitude + 180) % 360) - 180).compute()
            lat = ds.latitude.compute()

            region_mask = (
                (lon >= lon_min) & (lon <= lon_max) &
                (lat >= lat_min) & (lat <= lat_max)
            )

            yy, xx = np.where(region_mask.values)

            if len(yy) == 0 or len(xx) == 0:
                raise ValueError("No HRRR grid points found inside the selected map region.")

            y0, y1 = yy.min(), yy.max() + 1
            x0, x1 = xx.min(), xx.max() + 1

            rain_region = rain_inches.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).compute()

            lon_region = lon.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).values

            lat_region = lat.isel(
                y=slice(y0, y1, skip),
                x=slice(x0, x1, skip)
            ).values

            rain_values = rain_region.values

        # --- MAKE MAP ---
        fig = plt.figure(figsize=(9, 7))
        ax = plt.axes(projection=ccrs.PlateCarree())

        ax.set_extent([lon_min, lon_max, lat_min, lat_max], crs=ccrs.PlateCarree())

        ax.add_feature(cfeature.STATES.with_scale("50m"), linewidth=0.5)
        ax.add_feature(cfeature.COASTLINE.with_scale("50m"))
        ax.add_feature(cfeature.BORDERS.with_scale("50m"))

        mesh = ax.pcolormesh(
            lon_region,
            lat_region,
            rain_values,
            transform=ccrs.PlateCarree(),
            cmap="Blues",
            vmin=0,
            vmax=1.0,
            shading="auto"
        )

        cbar = plt.colorbar(mesh, ax=ax, shrink=0.75)
        cbar.set_label("1-hour Rainfall (inches)")

        plt.title(f"HRRR 1-hour Rainfall | Ending {t_step:%Y-%m-%d %H}Z")

        plt.savefig(png_path, bbox_inches="tight", dpi=120)
        plt.close("all")

        img_files.append(png_path)
        print(f"✅ Saved image: {png_path}")

        del rain_mm, rain_inches, rain_region, rain_values, lon_region, lat_region
        gc.collect()

    except Exception as e:
        print(f"⚠️ Failed rain {timestamp_str}: {e}")
        plt.close("all")

